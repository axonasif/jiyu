/**
 * The window.navigator object contains information about the visitor's browser (source: https://www.w3schools.com/js/js_window_navigator.asp).
 *
 * These code are meant to be executed in a browser.
 */

// Detect if cookie is enabled in user's browser
navigator.cookieEnabled;

// Detect if user's browser is online
navigator.onLine;

// Detect if user's browser is online
navigator.offline;

// Detect user's platform (OS)
navigator.platform;

// Detect a language in a browser
navigator.language;
